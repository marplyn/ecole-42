#include "../../so_long.h"

int	ft_strlen(const char *str, int type)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	if (type == 1)
	{
		if (i > 0 && str[i - 1] == '\n')
			i--;
	}
	return (i);
}
